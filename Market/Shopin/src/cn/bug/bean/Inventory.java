package cn.bug.bean;

/**
 * 库存表
 * @author 远朋
 *
 */
public class Inventory {
	private int iid;		//库存编号
	private Goods goods;	//商品信息
	private	String gsize;	//商品尺寸
	private int gnum;		//库存量
	
	public Inventory() {
		super();
	}
	public Inventory(int iid, Goods goods, String gsize, int gnum) {
		super();
		this.iid = iid;
		this.goods = goods;
		this.gsize = gsize;
		this.gnum = gnum;
	}
	public int getIid() {
		return iid;
	}
	public void setIid(int iid) {
		this.iid = iid;
	}
	public Goods getGoods() {
		return goods;
	}
	public void setGoods(Goods goods) {
		this.goods = goods;
	}
	public String getGsize() {
		return gsize;
	}
	public void setGsize(String gsize) {
		this.gsize = gsize;
	}
	public int getGnum() {
		return gnum;
	}
	public void setGnum(int gnum) {
		this.gnum = gnum;
	}
	
}
